    void llenaJustificantes(){
    controlador.Consultas consjust;
    consjust = new controlador.Consultas();
        CB_justificante.removeAllItems();
        
        Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(consjust.consultaCatalogoJudtificanteTotal());
            
            while (rs.next()){
                CB_justificante.addItem(rs.getString(1));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(RegistrarEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        Llenarmotivo(Integer.parseInt(CB_justificante.getItemAt(CB_justificante.getSelectedIndex())));
    }
    void Llenarmotivo(int clave){
    controlador.Consultas consJu;
    consJu = new controlador.Consultas();
    Statement st;
    try{
        st = cn.createStatement();
        ResultSet rs = st.executeQuery(consJu.consutaMotivoJus(clave));
            
             while(rs.next()){
                txt_Motivo.setText(rs.getString(1));
             }
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaEmpleados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void Actualiza(){
        Justificantes datosJusitficantes  = new Justificantes();
        controlador.Inserciones consJus  = new controlador.Inserciones();
        datosJusitficantes.setNIDTPJU(Integer.parseInt(CB_justificante.getItemAt(CB_justificante.getSelectedIndex())));
        datosJusitficantes.setCDESJUS(txt_Motivo.getText());
        datosJusitficantes.setCSTATUS(CB_Status.getItemAt(CB_Status.getSelectedIndex()));
        try {
                PreparedStatement pps = cn.prepareStatement(consJus.ModificarJustificante());
                pps.setString(1, datosJusitficantes.getCDESJUS());
                pps.setString(2, datosJusitficantes.getCSTATUS());
                pps.setInt(3, datosJusitficantes.getNIDTPJU());
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Modificado correctamente");
        }catch(SQLException ex) {
                Logger.getLogger(ConsultaEmpleados.class.getName()).log(Level.SEVERE, null, ex);
            }
    }

