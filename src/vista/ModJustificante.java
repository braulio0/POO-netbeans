    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Justificantes datosJusitficantes  = new Justificantes();
        datosJusitficantes.setNIDTPJU(Integer.parseInt(CB_justificante.getItemAt(CB_justificante.getSelectedIndex())));
        controlador.Deletes deleHor = new controlador.Deletes();
        try {
             PreparedStatement pps = cn.prepareStatement(deleHor.DeteleJustificante());
             pps.setInt(1, datosJusitficantes.getNIDTPJU());
             pps.executeUpdate();
             JOptionPane.showMessageDialog(this, "Justificante eliminado con exito");
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Fallo al eliminar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

